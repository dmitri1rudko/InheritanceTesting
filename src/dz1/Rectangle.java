package dz1;

public class Rectangle
{

    int width, height;


    public void setWidth(int width)
    {
        this.width = width;
    }

    public void setHeight(int height)
    {
        this.height = height;
    }

    public int getWidth()
    {
        return width;
    }

    public int getHeight()
    {
        return height;
    }

    public int rectangleSquare(int width, int height)
    {
        return width * height;
    }
}

